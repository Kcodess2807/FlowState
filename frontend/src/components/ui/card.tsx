  HTMLHeadingElement,
  React.HTMLAttributes<HTMLHeadingElement>
>(({ className, ...props }, ref) => (
  <h3
    ref={ref}
    className={cn(
      "font-display text-lg font-semibold tracking-tight text-ink",
      className,
    )}
    {...props}
  />
));
CardTitle.displayName = "CardTitle";

